import UIKit

extension UIFont {
    
    static func sf_pro_rounded_regular(size: CGFloat) -> UIFont? {
        return UIFont(name: "SF Pro Rounded Regular", size: size)
    }
    
    static func sf_pro_rounded_bold(size: CGFloat) -> UIFont? {
        return UIFont(name: "SF Pro Rounded Bold", size: size)
    }
}
